import UIKit

extension UIColor {
    public class var primaryColor: UIColor {
        return UIColor(hue: 86.0/255.0, saturation: 0.54, brightness: 0.51, alpha: 1.0)
    }
    
    public class var accentColor: UIColor {
        return UIColor(hue: 86.0/255.0, saturation: 0.75, brightness: 0.92, alpha: 1.0)
    }
}
